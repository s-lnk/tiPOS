int PrintLogo(int iPrintTimes)
{
	int iRet, iTemp;
	BYTE strTemp[200];
	BYTE byRet;
	WORD wTime2, wTime3, wTime;

	Lib_LcdCls();
	Lib_LcdGotoxy(0, 0);
	Lib_Lcdprintf("Print LOGO...");
	iTemp = iPrintTimes;
	while (1)
	{
		iRet = Lib_PrnInit();
		Lib_PrnLogo(g_Display_sinahome1);//g_Display_prtlogo2);//g_Display_sinahome1);
		iRet = Lib_PrnStr((BYTE*)"\n\n\n\n\n\n\n\n");
		while (1)
		{
			Lib_SetTimer(PRN_TM, 10000);
			wTime2 = Lib_CheckTimer(PRN_TM);
			iRet = Lib_PrnStart();
			CheckRet(35, iRet);

			
			if (PRN_TOOHEAT != iRet)
			{
				break;
			}
			else
			{
				Lib_LcdCls();
				Lib_LcdGotoxy(0, 20);
				Lib_LcdPrintfCE("Printer too\n heart to protect!", "Printer too\n heart to protect!");
				Lib_DelayMs(200);
				Lib_LcdCls();
				if (0 == Lib_KbCheck())
				{
					byRet = Lib_KbGetCh();
					if (KEYCANCEL == byRet)
					{
						return 0;
					}
				}
			}
		}
		wTime3 = Lib_CheckTimer(PRN_TM);
		Lib_LcdCls();
		Lib_LcdGotoxy(0, 0); 
		Lib_LcdGotoxy(0, 16);
		wTime = wTime2-wTime3;
		Lib_Lcdprintf("Time=%d.%d S\n", wTime/10, wTime%10); 
		switch (iPrintTimes)
		{
		case 0:
			if (0 != iRet)
			{
				return 0;
			}
			if (0 == Lib_KbCheck())
			{
				byRet = Lib_KbGetCh();
				if (KEYCANCEL == byRet)
				{
					return 0;
				}
			}
			break;
		case 1:
			Lib_LcdGotoxy(0, 48);
			Lib_Lcdprintf("Go on/Cancel£¿");
			while (1)
			{
				byRet = Lib_KbGetCh();
				if (KEYCANCEL == byRet)
				{
					return 0;
				}
				else if (KEYENTER == byRet)
				{
					break;
				}
			}
			break;
		default:
			if (0 != iRet)
			{
				return 0;
			}
			iTemp--;
			if (iTemp <= 0)
			{
				(void)Lib_KbGetCh();
				return 0;
			}
			if (0 == Lib_KbCheck())
			{
				byRet = Lib_KbGetCh();
				if (KEYCANCEL == byRet)
				{
					return 0;
				}
			}
		}
	}
}


